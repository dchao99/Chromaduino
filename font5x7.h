typedef struct fontProperties {
  byte ch_width;
  byte ch_height;
  byte ch_start;
  byte ch_total;
  int page_width;
} FontProperties;

FontProperties font;
static byte ch_cache = 0;  //= NUL
static byte font_cache[]= {0x00, 0x00, 0x00, 0x00, 0x00};

// Read font properties
void font_Init()
{
  font.ch_width = pgm_read_byte(&font5x7[0]);
  font.ch_height = pgm_read_byte(&font5x7[1]);
  font.ch_start = pgm_read_byte(&font5x7[2]);  
  font.ch_total = pgm_read_byte(&font5x7[3]);
  font.page_width = pgm_read_byte(&font5x7[4])*100 + pgm_read_byte(&font5x7[5]);
  
  DEBUG_PRINT( "Font Width:"+String(font.ch_width)+" Height:"+String(font.ch_height)+
                  " Range:"+String(font.ch_start)+"+"+String(font.ch_total)+" Page Width:"+
                  String(font.page_width)+"\n" );
}

byte font_Height()
{
  return font.ch_height;
}

// Read font map into cache and returns the proportional width of the font
int font_CheckCache(byte ch)
{
  static int prop_w;

  if ((ch < font.ch_start) || (ch > font.ch_start+font.ch_total-1))
    return 0;
  if ( ch != ch_cache )
  {
    int index =  ((ch - font.ch_start) * font.ch_width) + 6;      

    memcpy_P(font_cache, font5x7+index, font.ch_width);
    ch_cache = ch;
    prop_w = font.ch_width+1; // including a gap

    // Format proportional fonts, remove left/right blank columns
    if (font_cache[0] == 0x00)
    {
      int i;
      for (i=0; i<font.ch_width-1; i++)
        font_cache[i] = font_cache[i+1];
      font_cache[i] = 0x00;
      prop_w--;
      if (font_cache[3] == 0x00)
        prop_w--;
    } else
    if (font_cache[4] == 0x00)
      prop_w--;
  }
  return prop_w;
}

// returns true if (row,col) cell is ON in font. (0,0) is bottom left
bool font_GetCell(byte ch, int row, int col)
{  
  if ((ch < font.ch_start) || (ch > font.ch_start+font.ch_total-1))
    return false;
  return (col < font.ch_width) ? font_cache[col] & (0x01 << row) : false;
}

void DisplayChar(int matrix, byte ch, int width, int row, int col)
{
  // copies the characters font definition into the Buffer at matrix, row, col  
  for (int r = 0; r < font.ch_height; r++)
    for (int c = 0; c < width; c++)
      if ( 0 <= (r+row) && (r+row) < ColorduinoScreenHeight && 
           ColorduinoScreenWidth*matrix <= (c+col) && 
           (c+col) < ColorduinoScreenWidth*(matrix+1) ) 
      {
        mDisplay[matrix][r+row][(c+col)%ColorduinoScreenWidth] = font_GetCell(ch, r, c)?1:0;
      }
}

